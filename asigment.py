# Simultaneous Assignment
x,y = 100,200
result = x + y
print(result)

#output 
# 300
# ========================================================================================================

# Compound Assignment Operators
num = 200
num += 100
print(num)
num -= 100
print(num)
num *= 20
print(num)
num /= 2
print(num)

#output
# 300
# 200
# 4000
# 2000.0